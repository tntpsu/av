class TestRLSConvergence:
    def test_converges_to_true_L_from_below(self):
        """Init L=2.5, true L=3.0 → RLS should converge to ~3.0."""
        params = _make_params(wheelbase_m=2.5)
        ctrl = MPCController.__new__(MPCController)
        ctrl.params = params
        ctrl.solver = None  # not needed for _update_leff
        ctrl._leff_theta = 1.0 / 2.5
        ctrl._leff_P = params.leff_initial_P
        ctrl._leff_heading_prev = 0.0
        ctrl._leff_steering_prev = 0.0
        ctrl._leff_speed_prev = 0.0
        ctrl._leff_update_count = 0

        _simulate_heading_sequence(ctrl, true_L=3.0, n_frames=500)
        assert abs(ctrl.leff - 3.0) < 0.15, f"L_eff={ctrl.leff:.3f}, expected ~3.0"

    def test_converges_to_true_L_from_above(self):
        """Init L=2.5, true L=2.0 → RLS should converge to ~2.0."""
        params = _make_params(wheelbase_m=2.5)
