class TestInnovationSign:
    def test_innovation_positive_when_model_understeers(self):
        """
        When L_eff < true L, model predicts MORE heading change than reality
        (overestimates 1/L → overestimates steer gain). Wait — actually:
        θ = 1/L. If L_model < L_true, then θ_model > θ_true.
        Predicted heading = prev + θ_model * h - κ*v*dt
        Actual heading = prev + θ_true * h - κ*v*dt
        Innovation = actual - predicted = (θ_true - θ_model) * h
        Since θ_model > θ_true and h > 0 (positive steer, positive speed):
        Innovation = negative.
        """
        params = _make_params(wheelbase_m=2.0)  # L_model = 2.0 < L_true = 3.0
        ctrl = MPCController.__new__(MPCController)
        ctrl.params = params
        ctrl.solver = None
        ctrl._leff_theta = 1.0 / 2.0  # θ_model = 0.5 > θ_true = 0.333
        ctrl._leff_P = params.leff_initial_P
        ctrl._leff_heading_prev = 0.0
        ctrl._leff_steering_prev = 0.15  # positive steering
        ctrl._leff_speed_prev = 12.0
        ctrl._leff_update_count = 0

        # Generate one frame with true L=3.0
        true_L = 3.0
        dt = 0.033
        kappa = 0.005
        speed = 12.0
        delta_norm = 0.15
        max_steer = params.max_steer_rad

        actual_heading = 0.0 + (speed / true_L) * delta_norm * max_steer * dt - kappa * speed * dt
        result = ctrl._update_leff(actual_heading, speed, delta_norm, kappa, dt)

        # With L_model < L_true, θ_model > θ_true, h > 0:
        # innovation = actual - predicted should be negative
        assert result['leff_innovation'] < 0, (
            f"Innovation should be negative when model understeers, got {result['leff_innovation']:.6f}"
        )


