def _simulate_heading_sequence(ctrl: MPCController, true_L: float,
                                n_frames: int, speed: float = 12.0,
                                kappa: float = 0.005, delta_norm: float = 0.15,
                                dt: float = 0.033) -> None:
    """
    Feed the controller a sequence of heading measurements generated by
    a bicycle model with the TRUE wheelbase, so RLS can learn it.
    """
    e_heading = 0.0
    e_lat = 0.0
    max_steer = ctrl.params.max_steer_rad

    for _ in range(n_frames):
        # True dynamics: e_heading += (v/L_true) * δ * δ_max * dt - κ * v * dt
        e_heading_new = (
            e_heading
            + (speed / true_L) * delta_norm * max_steer * dt
            - kappa * speed * dt
        )
        e_lat_new = e_lat + speed * e_heading * dt

        ctrl._update_leff(e_heading_new, speed, delta_norm, kappa, dt)
        e_heading = e_heading_new
        e_lat = e_lat_new


